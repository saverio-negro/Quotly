import SwiftUI

extension ShapeStyle where Self == Color {
    static var darkBackground: Color {
        return Color(red: 0.03, green: 0.06, blue: 0.17)
    }
    
    static var darkBlue: Color {
        return Color(red: 0.11, green: 0.10, blue: 0.33)
    }
    
    static var aquamarine: Color {
        return Color(red: 0.33, green: 0.36, blue: 0.57)
    }
    
    static var lightBlue: Color {
        return Color(red: 0.57, green: 0.56, blue: 0.76)
    }
}
